def phi(m):
    result = m
    p = 2  # Починаємо з першого простого числа
    
    while p * p <= m:
        # Перевіряємо, чи p є дільником m
        if m % p == 0:
            # Якщо p ділить m, то потрібно відняти кратні p із результату
            while m % p == 0:
                m //= p
            result -= result // p
        p += 1
    
    # Якщо m > 1, то m - простий дільник
    if m > 1:
        result -= result // m
    
    return result

# Тестування функції на прикладі m = 13
m = 13
print(f"Значення функції Ейлера для m = {m}: {phi(m)}")