def gcdex(a, b):
    # Ініціалізація значень для розширеного алгоритму Евкліда
    x0, x1, y0, y1 = 1, 0, 0, 1
    while b != 0:
        # Обчислюємо частку та залишок
        q, a, b = a // b, b, a % b
        # Оновлюємо коефіцієнти x та y
        x0, x1 = x1, x0 - q * x1
        y0, y1 = y1, y0 - q * y1
    # Повертаємо НСД та коефіцієнти x0 і y0
    return a, x0, y0

# Тестування функції на прикладі a = 612 і b = 342
a, b = 612, 342
d, x, y = gcdex(a, b)
print(d, x, y)