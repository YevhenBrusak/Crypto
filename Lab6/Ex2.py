def gf_multiply(byte1, byte2):
    """
    Функція для множення двох байтів у полі Галуа GF(2^8) з редукцією по поліному m(x) = x^8 + x^4 + x^3 + x + 1.
    Використовує послідовний зсув і XOR для реалізації множення.
    """
    result = 0
    for i in range(8):  # Проходимо через кожен біт другого байта (byte2)
        if byte2 & 0x01:  # Перевіряємо, чи встановлений наймолодший біт
            result ^= byte1  # Додаємо byte1 до результату, якщо біт встановлений
        carry = byte1 & 0x80  # Перевіряємо, чи встановлений найстарший біт у byte1
        byte1 <<= 1  # Зсуваємо byte1 вліво на 1 біт
        if carry:  # Якщо найстарший біт був встановлений
            byte1 ^= 0x1B  # Здійснюємо редукцію з використанням полінома m(x)
        byte2 >>= 1  # Зсуваємо byte2 вправо на 1 біт, щоб перейти до наступного біта
    return result & 0xFF  # Повертаємо лише останні 8 біт результату

