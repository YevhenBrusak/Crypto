def mul02(byte):
    """
    Функція для множення байта на 02 в полі Галуа GF(2^8) з використанням зсуву та операції XOR.
    Якщо найстарший біт (8-й) дорівнює 1, відбувається редукція за допомогою полінома m(x) = x^8 + x^4 + x^3 + x + 1.
    """
    result = byte << 1  # Лівий зсув на 1 біт (еквівалент множення на x)
    if (byte & 0x80) != 0:  # Перевіряємо, чи встановлений найстарший біт
        result ^= 0x1B  # Здійснюємо редукцію за допомогою полінома m(x)
    return result & 0xFF  # Повертаємо лише останні 8 біт результату

def mul03(byte):
    """
    Функція для множення байта на 03 в полі Галуа GF(2^8).
    Використовує mul02 для множення на 02, а потім додає початковий байт.
    """
    return mul02(byte) ^ byte  # Множення на 02 і додавання початкового байта

# Тести
print(hex(mul02(0xD4)))
print(hex(mul03(0xBF)))